package hello.c_xmlToJava;
//Xml属性对象 
public class Attribute {

    private String name;  
    private String value;  
      
    public String getName() {  
        return name;  
    }  
    public void setName(String name) {  
        this.name = name;  
    }  
    public String getValue() {  
        return value;  
    }  
    public void setValue(String value) {  
        this.value = value;  
    }  
}  
